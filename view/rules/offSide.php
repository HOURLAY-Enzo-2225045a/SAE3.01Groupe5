<h1 class="text-4xl font-serif">Le hors jeu</h1>
<p class="text-3xl font font-serif">
    Un joueur qui attaque n’a pas le droit de précéder le palet dans la zone d’attaque.<br>
    C’est ce qu’on appelle le hors-jeu. Autre règle très importante, celle du dégagement interdit. <br>
    On appelle un dégagement interdit quand le palet tiré depuis la demie défensive franchit la ligne de but adverse alors que les deux équipes sont à force égale ou que l’équipe qui attaque est en supériorité numérique.
</p>

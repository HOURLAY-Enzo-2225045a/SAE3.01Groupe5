<h2 class="font-serif text-3xl">Ici je vais t’apprendre les basiques du hockey sur glace.<br>
    Alors enfile tes patins et c’est parti !</h2>
<p class="text-3xl text-gray-600 mb-4 font-serif">Le hockey sur glace est un sport qui se pratique à <span
            class="font-mono">6 contre 6</span>, avec <span class="font-mono">1 gardien</span> et <span
            class="font-mono">5 joueurs</span> par équipe.<br>
    L’objectif du jeu est de marquer un maximum de buts en envoyant un disque en caoutchouc appelé <span
            class="font-mono">palet</span>, dans le but adverse.<br>
    Pour manipuler le palet, les joueurs utilisent une <span class="font-mono">crosse de hockey</span>.<br>
    Le hockey sur glace est le seul sport où les joueurs peuvent se déplacer derrière les buts !<br>
    Le terrain de jeu, appelé patinoire, mesure entre 56 et 60 mètres de long, et entre 26 à 30 mètres de large.<br>
    Il est entouré par des balustrades.<br></p>

<h1>Jeu</h1>

<p>Un match de hockey sur glace se déroule en 3 périodes de 15 à 20 minutes, selon les catégories d’âge, avec une pause de 15 minutes entre chacune d’entre elles.
L’équipe qui marque le plus de buts au cours de la rencontre est déclarée vainqueur.
Certains matches peuvent être complétés d’une prolongation, en cas de match nul entre les deux équipes.
Si celle-ci ne suffit pas à les départager, commence alors une séance de tirs au but.
Pour gêner l’adversaire, les hockeyeurs ont le droit de rentrer en contact avec lui pour le déstabiliser et récupérer le palet.
    En cas de geste interdit, un joueur peut être exclu de la glace pendant plusieurs minutes, laissant son équipe en infériorité numérique le temps de son exclusion.

Dans un match, une équipe peut avoir au maximum 20 joueurs de champ + 2 gardiens
Il y a forcément un capitaine par équipe + 2 capitaine assistant maximum 
    Le capitaine porte un “C” et les assistant un “A” bien visible sur le devant du maillot pour les identifier.
</p>


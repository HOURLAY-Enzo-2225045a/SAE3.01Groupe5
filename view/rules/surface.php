<h1 class="text-2xl font-semibold text-gray-800 mb-2">Surface de jeu</h1>
<p class="text-gray-600 mb-4">La patinoire est divisée en 3 zones, délimitées par 2 lignes bleues: la zone d’attaque, la
    zone neutre (au milieu) et la zone de défense.</p>
<p class="text-gray-600 mb-4">La zone de défense est comprise entre la balustrade derrière le but défendu par l’équipe
    et la première ligne bleue. Cette zone comprend également une ligne rouge fine, appelée ligne de but, sur laquelle
    est placé le but à défendre.</p>
<p class="text-gray-600 mb-4">La zone neutre, quant à elle, est la partie de la patinoire comprise entre les deux lignes
    bleues. Au milieu de la zone, une ligne rouge aussi épaisse que les lignes bleues symbolise le milieu de la glace et
    est appelée la ligne rouge centrale. </p>
<p class="text-gray-600 mb-10">Enfin, la zone d’attaque va de la deuxième ligne bleue à la
    bande derrière le but défendu par l’équipe adverse.
    Elle comprend également l’autre ligne de but de la patinoire,
    sur laquelle est positionné le but adverse.</p>